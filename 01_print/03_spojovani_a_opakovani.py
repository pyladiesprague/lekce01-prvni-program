# Spusť soubor a porovnej výsledky.


# Text jde spojit pomocí + (plus). Pozor na rozdíl oproti čárce:
#   čárka mezi věci přidá mezeru
#   plus je spojí natěsno (mezeru si musíš přidat sama)
print("Ahoj", "PyLadies!")         # Ahoj PyLadies!
print("Ahoj" + "PyLadies!")        # AhojPyLadies!
print("Ahoj" + " " + "PyLadies!")  # Ahoj PyLadies!


# Hvězdičkou se text opakuje – hodí se třeba na oddělovací čáru:
print("=" * 20)
print("ahoj " * 3)
