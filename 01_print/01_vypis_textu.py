# Co je print?
# print je funkce, která vypíše na obrazovku to, co jí zadáš.
# Je to náš hlavní způsob, jak si ukázat, co program dělá –
# bude nás provázet celým kurzem.
#
# Zápis:  print(neco)


# Klasický první program:
print("Ahoj světe!")


# Text (říkáme mu řetězec) musíme obalit do uvozovek.
print("Máma má mísu")


# Uvozovky můžou být dvojité "..." nebo jednoduché '...'.
# Pythonu je to jedno, obojí funguje stejně.
print('Máma má mísu')


# Pozor na drobnost:
# mezi slovo print a závorku se mezera NEpíše.
