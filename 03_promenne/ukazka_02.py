# ---------------------------------------------
#  K čemu jsou proměnné dobré
# ---------------------------------------------
# Spusť soubor a sleduj, co se vypíše.


# Proměnnou můžeš použít víckrát. Hodí se, když se něco opakuje.
jmeno = "Anna"
print("Ahoj,", jmeno)
print(jmeno, "vítej v Pythonu!")
print("Měj se hezky,", jmeno)

# Zkus změnit jméno na jednom místě (řádek s jmeno = ...)
# a spusť znovu – změní se to úplně všude. To je kouzlo proměnných.


# Funguje to i s počítáním.
# S proměnnými se dá počítat úplně stejně jako s čísly:
a = 5
b = 3
print("Součet je", a + b)


# Příklad se čtvercem: stranu zadáme jen jednou do proměnné.
strana = 123
print("Obvod čtverce se stranou", strana, "je", 4 * strana, "cm")
print("Obsah čtverce se stranou", strana, "je", strana * strana, "cm²")

# Kdybychom chtěli jiný čtverec, stačí změnit jen řádek strana = ...
# a oba výpočty se přepočítají samy.
