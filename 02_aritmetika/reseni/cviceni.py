# Tvoje řešení může vypadat trochu jinak, a to je v pořádku.


# 1) Vypiš, kolik je 7 + 15.
print(7 + 15)


# 2) Vypiš, kolik je 9 krát 6.
print(9 * 6)


# 3) Vypiš, kolik je 100 děleno 8.
print(100 / 8)


# 4) Vypiš celočíselný podíl 100 a 8.
print(100 // 8)


# 5) Vypiš zbytek po dělení 100 sedmi.
print(100 % 7)


# 6) Vypiš 3 na čtvrtou.
print(3 ** 4)


# 7) Vypiš větu i s výsledkem, třeba: Dohromady to je 25
#    Číslo nech spočítat Python (napiš do print výpočet, ne hotové číslo).
