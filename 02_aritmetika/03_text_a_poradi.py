# ---------------------------------------------
#  Počítání s textem a pořadí operací
# ---------------------------------------------
# Spusť soubor a sleduj výsledky.


# Počítat můžeš i uvnitř print, klidně spolu s textem:
print("Součet čísel 3 a 8 je", 3 + 8)


# Když je operací víc, platí stejné pořadí jako v matematice:
# nejdřív násobení a dělení, potom sčítání a odčítání.
print(2 + 3 * 4)     # 14  (nejdřív 3 * 4, pak + 2)


# Závorkami změníš pořadí:
print((2 + 3) * 4)   # 20  (nejdřív 2 + 3, pak * 4)


# Závorky fungují úplně stejně jako v matematice:
print(3 + (4 + 6) * 8 / 2 - 1)
