# ---------------------------------------------
#  Další operátory (// % **)
# ---------------------------------------------
# Kromě + - * / má Python i pár operátorů navíc.
# Teď je jen potkáme a vyzkoušíme – pořádně se nám budou hodit
# později (třeba u cyklů). Když ti hned nedojde, k čemu jsou, nevadí.
# Spusť soubor a sleduj výsledky.


# Celočíselné dělení //
# Vydělí a zahodí zbytek – vrátí jen celý podíl (zaokrouhlí dolů).
print(17 // 5)   # 3  (17 děleno 5 je 3 a něco, celá část je 3)


# Zbytek po dělení % (říká se mu modulo)
# Vrátí to, co po dělení zbyde.
print(17 % 5)    # 2  (17 = 3 * 5 + 2, zbytek je 2)


# Mocnina **
# 2 ** 3 znamená „dvě na třetí", tedy 2 * 2 * 2.
print(2 ** 3)    # 8
print(5 ** 2)    # 25
